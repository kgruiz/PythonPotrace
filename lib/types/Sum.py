# Sum.py


class Sum:
    def __init__(self, x, y, xy, x2, y2):
        """
        Initialize sum values for calculations.

        Parameters
        ----------
        x : float
            Sum of x values.
        y : float
            Sum of y values.
        xy : float
            Sum of x*y products.
        x2 : float
            Sum of x squared.
        y2 : float
            Sum of y squared.
        """
        self.x = x
        self.y = y
        self.xy = xy
        self.x2 = x2
        self.y2 = y2
